    private void btCadastrarEntregaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCadastrarEntregaActionPerformed
        // TODO add your handling code here:
        
        Cliente c = null;
        
        for(int i=0; i<clientes.size(); i++){
            if(jcClientes.getSelectedItem().toString().equals(clientes.get(i).getNome().toString())){
                c = new Cliente(clientes.get(i).getId(), clientes.get(i).getNome(), clientes.get(i).getNumero(), clientes.get(i).getBairro(), clientes.get(i).getLatitude_cliente(), clientes.get(i).getLongitude_cliente(), clientes.get(i).isVip());
            }
        }
        
        Produto p = null;
        
        for(int i=0; i<produtos.size(); i++){
            if(jcProdutos.getSelectedItem().toString().equals(produtos.get(i).getNome().toString())){
                p = new Produto(produtos.get(i).getId(), produtos.get(i).getNome(), produtos.get(i).getQuantidade(), produtos.get(i).getPrice());
            }
        }
        
        int id = idEntrega;
        String brinde = jtBrinde.getText().toString();
        
        Entrega e = new Entrega(contEntrega, c.getId(), c.getNumero(), c.getNome(), c.getBairro(), c.getLatitude_cliente(), c.getLongitude_cliente(), c.isVip(), p.getId(), p.getNome(), p.getQuantidade(), p.getPrice(), brinde);
        entregas.add(e);
        
        EntregaTableModel modelEntrega = new EntregaTableModel(entregas);
        tabelaEntregas.setModel(modelEntrega);
        
        contEntrega++;
        
        JOptionPane.showMessageDialog(null, "Cadastrado com sucesso!");
        
    }//GEN-LAST:event_btCadastrarEntregaActionPerformed

