    private void btMarcarEntregaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btMarcarEntregaActionPerformed
        // TODO add your handling code here:

        int idCli=0;
        int numCli=0;
        String nomeCli=null;
        String bairroCli=null;
        double longi=0;
        double lat=0;
        boolean vip=true;
        int idProd=0;
        String nomeProd=null;
        int qtdProd=0;
        double precoProd=0;
        String brinde=null;
        
        for(int i=0; i<entregas.size(); i++){
            if(idEntrega == entregas.get(i).getId()){
                idCli = entregas.get(i).getIdCliente();
                numCli = entregas.get(i).getNumeroCliente();
                nomeCli = entregas.get(i).getNomeCliente();
                bairroCli = entregas.get(i).getBairroCliente();
                longi = entregas.get(i).getLongitude_cliente();
                lat = entregas.get(i).getLatitude_cliente();
                vip = entregas.get(i).isVip();
                idProd = entregas.get(i).getIdProduto();
                nomeProd = entregas.get(i).getNomeProduto();
                qtdProd = entregas.get(i).getQtdProduto();
                precoProd = entregas.get(i).getPrecoProduto();
                brinde = entregas.get(i).getBrinde();
            }
        }
        
        double distancia = lat + longi;
        
        EntregaR e = new EntregaR(contEntregaRealizada, idCli, numCli, nomeCli, bairroCli, distancia, vip, idProd, nomeProd, qtdProd, precoProd, brinde);
        entregasRealizadas.add(e);
        
        EntregaRealizadaTableModel modelEntregaRealizada = new EntregaRealizadaTableModel(entregasRealizadas);
        tabelaEntregasRealizadas.setModel(modelEntregaRealizada);
        
        entregas.remove(idEntrega);
        
        EntregaTableModel modelEntrega = new EntregaTableModel(entregas);
        tabelaEntregas.setModel(modelEntrega);
        
        contEntregaRealizada++;
        
    }//GEN-LAST:event_btMarcarEntregaActionPerformed

    
